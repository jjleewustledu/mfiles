%MLEMDATETIME
%
%  USAGE:  file_label = mlemDateTime(idx, metric)
%
%          idx is an integer index
%          metric is 'cbf', 'cbv' or 'mtt'
%
%  SYNOPSIS:
%
%  SEE ALSO:  'Cell Arrays of Strings' or 'strings, cells arrays of' in Matlab help.
%
%  $Id$
%________________________________________________________________________
function [fileLabel] = mlemDateTime(idx, metric)

cbfTimes = {... ...
	 		  'date_2006_4_28_time_2016' ...
	          'date_2006_4_28_time_1514' ...
	          'date_2006_5_1_time_316' ...
	          'date_2006_4_30_time_1657' ...
	          'date_2006_4_30_time_1227' ...
	          'date_2006_4_30_time_1125' ...
	          'date_2006_5_1_time_853' ...
	          'date_2006_5_1_time_411' ...
	          'date_2006_5_1_time_312' ...
	          'date_2006_4_28_time_141' ...
	          'date_2006_4_28_time_857' ...
	          'date_2006_4_28_time_544' ...
	          'date_2006_4_28_time_926' ...
	          'date_2006_4_28_time_843' ...
	          'date_2006_4_28_time_1712' ...
	          'date_2006_4_28_time_1828' ...
	          'date_2006_4_28_time_1032' ...
	          'date_2006_4_28_time_1629' ...
	          'date_2006_4_30_time_910' };         
         
sizes = size(cbfTimes);
len   = sizes(2);

switch metric
    case 'cbf'
        if (idx < 1)
            fileLabel = cbfTimes;          
        elseif (idx > len)    
            fileLabel = cbfTimes(len);
            fileLabel = char(fileLabel);
        else
            fileLabel = cbfTimes(idx);
            fileLabel = char(fileLabel);
        end
    otherwise
        error(['mlemDateTime:  could not find a ' metric ' data-file for patient ' idx]);
end
